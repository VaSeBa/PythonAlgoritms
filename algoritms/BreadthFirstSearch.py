# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º deque –∏–∑ –º–æ–¥—É–ª—è collections –¥–ª—è —ç—Ñ—Ñ–µ–∫—Ç–∏–≤–Ω–æ–π —Ä–∞–±–æ—Ç—ã —Å –æ—á–µ—Ä–µ–¥—å—é
from collections import deque

# –ì—Ä–∞—Ñ —Å–æ—Ü–∏–∞–ª—å–Ω—ã—Ö —Å–≤—è–∑–µ–π –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω –≤ –≤–∏–¥–µ —Å–ª–æ–≤–∞—Ä—è:
# - –ö–ª—é—á–∏: –∏–º–µ–Ω–∞ –ª—é–¥–µ–π
# - –ó–Ω–∞—á–µ–Ω–∏—è: —Å–ø–∏—Å–∫–∏ –∏—Ö –¥—Ä—É–∑–µ–π (—Å–æ—Å–µ–¥–µ–π –≤ –≥—Ä–∞—Ñ–µ)
# –ü—Ä–æ–¥–∞–≤—Ü—ã –º–∞–Ω–≥–æ –ø–æ–º–µ—á–µ–Ω—ã —Å—É—Ñ—Ñ–∏–∫—Å–æ–º '_seller'
graph = {
    'you': ['alice', 'bob', 'claire'],  # –£ –≤–∞—Å 3 –¥—Ä—É–≥–∞
    'alice': ['peggy_seller'],  # –ü—Ä–æ–¥–∞–≤–µ—Ü –Ω–∞ 1 —É—Ä–æ–≤–Ω–µ
    'bob': ['anuj_seller', 'peggy'],  # –ü—Ä–æ–¥–∞–≤–µ—Ü –Ω–∞ 1 —É—Ä–æ–≤–Ω–µ
    'claire': ['thom', 'jonny_seller'],  # –ü—Ä–æ–¥–∞–≤–µ—Ü –Ω–∞ 2 —É—Ä–æ–≤–Ω–µ
    'thom': [],
    'jonny_seller': [],
    'peggy_seller': [],
    'anuj_seller': [],
    'peggy': []
}


def is_mango_seller(person):
    """
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —á–µ–ª–æ–≤–µ–∫ –ø—Ä–æ–¥–∞–≤—Ü–æ–º –º–∞–Ω–≥–æ.
    –í —ç—Ç–æ–º –ø—Ä–∏–º–µ—Ä–µ –ø—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ —Å—É—Ñ—Ñ–∏–∫—Å—É '_seller'.
    –í —Ä–µ–∞–ª—å–Ω–æ–π –∑–∞–¥–∞—á–µ –∑–¥–µ—Å—å –º–æ–≥–ª–∞ –±—ã –±—ã—Ç—å —Å–ª–æ–∂–Ω–∞—è –ª–æ–≥–∏–∫–∞ –ø—Ä–æ–≤–µ—Ä–∫–∏.
    """
    return person.endswith('_seller')


def bfs_mango_seller(graph, start):
    """
    –ê–ª–≥–æ—Ä–∏—Ç–º –ø–æ–∏—Å–∫–∞ –≤ —à–∏—Ä–∏–Ω—É (BFS) –¥–ª—è –Ω–∞—Ö–æ–∂–¥–µ–Ω–∏—è –±–ª–∏–∂–∞–π—à–µ–≥–æ –ø—Ä–æ–¥–∞–≤—Ü–∞ –º–∞–Ω–≥–æ.
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –ø–µ—Ä–≤–æ–≥–æ –Ω–∞–π–¥–µ–Ω–Ω–æ–≥–æ –ø—Ä–æ–¥–∞–≤—Ü–∞ —Å —É–∫–∞–∑–∞–Ω–∏–µ–º —É—Ä–æ–≤–Ω—è —É–¥–∞–ª–µ–Ω–Ω–æ—Å—Ç–∏.
    """
    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –æ—á–µ—Ä–µ–¥—å —Å –Ω–∞—á–∞–ª—å–Ω—ã–º —É–∑–ª–æ–º –∏ —É—Ä–æ–≤–Ω–µ–º 0
    # –ö–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç –æ—á–µ—Ä–µ–¥–∏ - –∫–æ—Ä—Ç–µ–∂ (–∏–º—è, —É—Ä–æ–≤–µ–Ω—å)
    queue = deque()
    queue.append((start, 0))

    # –ú–Ω–æ–∂–µ—Å—Ç–≤–æ –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è —É–∂–µ –ø—Ä–æ–≤–µ—Ä–µ–Ω–Ω—ã—Ö –ª—é–¥–µ–π
    visited = set()
    visited.add(start)

    # –°—á–µ—Ç—á–∏–∫ –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è –æ–±—â–µ–≥–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –ø—Ä–æ–≤–µ—Ä–µ–Ω–Ω—ã—Ö –¥—Ä—É–∑–µ–π
    total_friends = 0

    print("üëâ –ù–∞—á–∏–Ω–∞–µ–º –ø–æ–∏—Å–∫ —Å:", start)

    # –û—Å–Ω–æ–≤–Ω–æ–π —Ü–∏–∫–ª –ø–æ–∏—Å–∫–∞: —Ä–∞–±–æ—Ç–∞–µ—Ç –ø–æ–∫–∞ –æ—á–µ—Ä–µ–¥—å –Ω–µ –ø—É—Å—Ç–∞
    while queue:
        # –ò–∑–≤–ª–µ–∫–∞–µ–º –ø–µ—Ä–≤—ã–π —ç–ª–µ–º–µ–Ω—Ç –∏–∑ –æ—á–µ—Ä–µ–¥–∏ (FIFO - First In First Out)
        current_person, level = queue.popleft()

        # –ï—Å–ª–∏ —Ç–µ–∫—É—â–∏–π —á–µ–ª–æ–≤–µ–∫ - –ø—Ä–æ–¥–∞–≤–µ—Ü, –∑–∞–≤–µ—Ä—à–∞–µ–º –ø–æ–∏—Å–∫
        if is_mango_seller(current_person):
            print(f"\nüîç –í—Å–µ–≥–æ –ø—Ä–æ–≤–µ—Ä–µ–Ω–æ –¥—Ä—É–∑–µ–π: {total_friends}")
            return f"üéâ –ë–ª–∏–∂–∞–π—à–∏–π –ø—Ä–æ–¥–∞–≤–µ—Ü: {current_person} (—É—Ä–æ–≤–µ–Ω—å {level})"

        # –ü–æ–ª—É—á–∞–µ–º —Å–ø–∏—Å–æ–∫ –¥—Ä—É–∑–µ–π —Ç–µ–∫—É—â–µ–≥–æ —á–µ–ª–æ–≤–µ–∫–∞
        friends = graph.get(current_person, [])

        # –û—Ç–ª–∞–¥–æ—á–Ω—ã–π –≤—ã–≤–æ–¥ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ —Ç–µ–∫—É—â–µ–º —à–∞–≥–µ
        print(f"\nüîé –£—Ä–æ–≤–µ–Ω—å {level}: –ø—Ä–æ–≤–µ—Ä—è–µ–º {current_person}")
        print(f"   –î—Ä—É–∑—å—è {current_person}: {friends}")

        # –î–æ–±–∞–≤–ª—è–µ–º –≤—Å–µ—Ö –¥—Ä—É–∑–µ–π —Ç–µ–∫—É—â–µ–≥–æ —á–µ–ª–æ–≤–µ–∫–∞ –≤ –æ—á–µ—Ä–µ–¥—å
        for friend in friends:
            if friend not in visited:
                visited.add(friend)  # –ü–æ–º–µ—á–∞–µ–º –∫–∞–∫ –ø–æ—Å–µ—â–µ–Ω–Ω–æ–≥–æ
                queue.append((friend, level + 1))  # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —É—Ä–æ–≤–µ–Ω—å –Ω–∞ 1
                total_friends += 1  # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á–µ—Ç—á–∏–∫ –ø—Ä–æ–≤–µ—Ä–æ–∫

    # –ï—Å–ª–∏ –æ—á–µ—Ä–µ–¥—å –ø—É—Å—Ç–∞ –∏ –ø—Ä–æ–¥–∞–≤–µ—Ü –Ω–µ –Ω–∞–π–¥–µ–Ω
    return "üòû –ü—Ä–æ–¥–∞–≤–µ—Ü –Ω–µ –Ω–∞–π–¥–µ–Ω"


# –ó–∞–ø—É—Å–∫–∞–µ–º –ø–æ–∏—Å–∫ –æ—Ç —É–∑–ª–∞ 'you'
result = bfs_mango_seller(graph, 'you')
print(result)
